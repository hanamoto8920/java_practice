public class math4 {
	public static void main(String[] args) {
		boolean a = true;
		boolean b = false;
		boolean c;
		System.out.println(c = a && true);
		System.out.println(c = a && false);
		System.out.println(c = b || true);
		System.out.println(c = b || false);
		System.out.println(c = !a);
		System.out.println(c = !b);
	}

}
